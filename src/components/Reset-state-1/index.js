// RESET THE STATES

// When you render a different component in the same position, it resets the state of its entire subtree. 

// To see how this works, increment the counter and then tick the checkbox:

import { useState } from 'react';
import './styles.css';

export default function ResettingStateOne() {
  const [isFancy, setIsFancy] = useState(false);
  return (
    <div>
      {isFancy ? (
        <div>
          <Counter isFancy={true} /> 
        </div>
      ) : (
        <section>
          <Counter isFancy={false} />
        </section>
      )}
      <label>
        <input
          type="checkbox"
          checked={isFancy}
          onChange={e => {
            setIsFancy(e.target.checked)
          }}
        />
        Use fancy styling
      </label>
    </div>
  );
}

function Counter({ isFancy }) {
  const [score, setScore] = useState(0);
  const [hover, setHover] = useState(false);

  let className = 'counter';
  if (hover) {
    className += ' hover';
  }
  if (isFancy) {
    className += ' fancy';
  }

  return (
    <div
      className={className}
      onPointerEnter={() => setHover(true)}
      onPointerLeave={() => setHover(false)}
    >
      <h1>{score}</h1>
      <button onClick={() => setScore(score + 1)}>
        Add one
      </button>
    </div>
  );
}


// The counter state gets reset when you click the checkbox. 
// Although you render a Counter, the first child of the div changes from a div to a section. 
// When the child div was removed from the DOM, the whole tree below it (including the Counter and its state) was destroyed as well.